// Bakes confirmed board equities from ll_equity_cache into src/data/equity-cache.js
// so the client ships them with zero runtime fetch, exactly like JAM_EQ. Only
// CONFIRMED rows are readable with the publishable key (RLS), so this needs no
// service secret. Run in the release flow after the aggregator, before build:
//   node tools/sim/bake-equity-cache.js
"use strict";
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..", "..");
const OUT = path.join(root, "src", "data", "equity-cache.js");

async function main() {
  const cfg = JSON.parse(fs.readFileSync(path.join(root, "src", "supabase-config.json"), "utf8"));
  // Keep in lockstep with the client's model version so we never bake stale-range rows.
  const src = fs.readFileSync(path.join(root, "src", "leak-lab.jsx"), "utf8");
  const m = src.match(/EQUITY_MODEL_V\s*=\s*(\d+)/);
  const rv = m ? +m[1] : 1;

  const rows = [];
  const PAGE = 1000;
  for (let from = 0; ; from += PAGE) {
    const url = `${cfg.url}/rest/v1/ll_equity_cache?confirmed=eq.true&rv=eq.${rv}&select=k,equity&order=k.asc&offset=${from}&limit=${PAGE}`;
    const r = await fetch(url, { headers: { apikey: cfg.publishableKey } });
    if (!r.ok) throw new Error(`fetch confirmed cache -> ${r.status} ${await r.text()}`);
    const page = await r.json();
    rows.push(...page);
    if (page.length < PAGE) break;
  }

  const map = {};
  for (const k of rows.map((x) => x.k).sort()) map[k] = +rows.find((x) => x.k === k).equity;
  const body = `// GENERATED by tools/sim/bake-equity-cache.js — confirmed board equities (rv ${rv}).
// Baked from ll_equity_cache so the client ships them with zero runtime fetch, like
// JAM_EQ. Consulted by boardEquity() only when EQUITY_CACHE_LIVE is true.
// Regenerate after the aggregator confirms new rows: npm run bake:equity
export const EQUITY_CACHE = ${JSON.stringify(map)};
`;
  fs.writeFileSync(OUT, body);
  console.log(`baked ${Object.keys(map).length} confirmed equities (rv ${rv}) -> src/data/equity-cache.js`);
}
main().catch((e) => { console.error(e.message || e); process.exit(1); });

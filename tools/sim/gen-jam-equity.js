// Generates src/data/jam-equity.js: per-profile equity of a hand at a sampled
// hero-percentile vs that profile's shove range (Monte Carlo). gradeStackoff
// interpolates this to price jam-pot mistakes at their true bb cost instead of a
// percentile-distance proxy. Regenerate when profile jamReps change:
//   node tools/sim/gen-jam-equity.js
"use strict";
const fs = require("fs");
const path = require("path");
const esbuild = require("esbuild");

const root = path.join(__dirname, "..", "..");
const tmp = path.join(__dirname, ".build");
fs.mkdirSync(tmp, { recursive: true });
fs.cpSync(path.join(root, "src"), tmp, { recursive: true });
fs.copyFileSync(path.join(tmp, "leak-lab.jsx"), path.join(tmp, "src.jsx"));
// Stub the data import — this generator produces it, so it can't depend on it.
fs.mkdirSync(path.join(tmp, "data"), { recursive: true });
fs.writeFileSync(path.join(tmp, "data", "jam-equity.js"), "export const JAM_EQ = {};\n");
fs.writeFileSync(path.join(tmp, "probe.jsx"),
  fs.readFileSync(path.join(tmp, "src.jsx"), "utf8") +
  "\nexport { mcEquity, RANKED, PROFILES, materialize };\n");
esbuild.buildSync({ entryPoints: [path.join(tmp, "probe.jsx")], bundle: true, format: "cjs",
  jsx: "automatic", loader: { ".jsx": "jsx" }, external: ["react", "react/jsx-runtime"], outfile: path.join(tmp, "probe.js"), logLevel: "silent" });
const M = require(path.join(tmp, "probe.js"));

// Percentile grid — dense near the top (where jam decisions live), sparse below.
const GRID = [0.2, 0.35, 0.55, 0.8, 1.15, 1.6, 2.1, 3, 4.5, 6.5, 10, 16, 25, 45, 75];
const TRIALS = 24000;
const rankedAsc = [...M.RANKED].sort((a, b) => a.pct - b.pct);
const nearest = (p) => rankedAsc.reduce((b, h) => (Math.abs(h.pct - p) < Math.abs(b.pct - p) ? h : b));

const out = {};
for (const p of M.PROFILES) {
  const rng = p.jamRep;
  const curve = GRID.map((gp) => {
    const h = nearest(gp);
    let s = 0, k = 4;
    for (let i = 0; i < k; i++) { const e = M.mcEquity(M.materialize(h), [], rng, TRIALS / k); if (e) s += e.equity; }
    return [gp, +(s / k).toFixed(3)];
  });
  out[p.id] = curve;
  process.stderr.write(`${p.id}: ${curve.map(([x, y]) => y.toFixed(2)).join(" ")}\n`);
}

const body = `// GENERATED by tools/sim/gen-jam-equity.js — per-profile [heroPercentile, equity]
// vs that profile's shove range (Monte Carlo). Interpolated by gradeStackoff to
// price jam-pot mistakes at their true bb cost. Regenerate if jamReps change.
export const JAM_EQ = ${JSON.stringify(out)};
`;
fs.writeFileSync(path.join(root, "src", "data", "jam-equity.js"), body);
console.log("src/data/jam-equity.js written");
